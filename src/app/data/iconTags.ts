// ===== HELPER FUNCTIONS =====

export function getAllThemes(): string[] {
  return [...THEMES];
}

export function getIconsByTheme(theme: string): string[] {
  return Object.entries(iconTags)
    .filter(([, tags]) => tags.includes(theme))
    .map(([iconName]) => iconName);
}

export function getThemesForIcon(iconName: string): string[] {
  return iconTags[iconName] || [];
}

export function getCompletionStats(): { total: number; withThemes: number } {
  const withThemes = Object.values(iconTags).filter(tags => tags.length > 0).length;
  return {
    total: Object.keys(iconTags).length,
    withThemes,
  };
}
